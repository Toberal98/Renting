			<div class="row no-gutters justify-content-center">
				<div class="col-md-10 col-lg-10 col-xl-10 p-2">
					<div class="title-head-sm-left">1- ¿Qué es Renting?</div><br>
					<p>Es la forma de estrenar carro nuevo constantemente sin necesidad de comprarlo, pagando una cuota que incluye todo, sin primas, pagos extras, simplemente lo adquieres con una cuota fija y te olvidas de todo.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">2- ¿Cuánto debo pagar de prima para un Renting?</div><br>
					<p>Renting no es la compra de un vehículo por lo que no es necesario que pagues prima, lo único que te pedimos es un depósito inicial, reembolsable al final del plazo con la entrega del vehículo.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">3- ¿Cuál es la tasa de interés? </div><br>
					<p>Renting no tiene tasa de interés, por lo tanto, no tienes que preocuparte por alzas en los mercados financieros. Es una cuota fija por el plazo de tiempo contratado; lo único que podría cambiar es el valor del seguro en caso de presentarse siniestros.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">4- ¿Qué incluye la cuota?</div><br>
					<p>Tu cuota de renting incluye: valor por el uso del vehículo, mantenimiento preventivo, seguro de daños propios y terceros, y gastos anuales de matrícula.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">5- ¿Qué incluye el mantenimiento?</div><br>
					<p>La cuota incluye el plan del mantenimiento preventivo según el manual de la marca y modelo que tú escojas, para realizarse cada 5,000 km en los talleres de expertos certificados. Puedes incluir dentro de tu contrato de mantenimiento cambio de llantas, batería, pastillas de freno, escobillas, entre otros, cada cierto kilometraje.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">6- ¿Qué gastos no están incluidos en la cuota?</div><br>
					<p>Si manejas de acuerdo con tus kilómetros contratados y de acuerdo con las buenas prácticas de conducción, deberían ser solo combustibles y gastos de piezas de desgaste no incluidas en tu contrato.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">7- ¿Qué piezas de desgaste si puedes incluir en tu contrato?</div><br>
					<p>Llantas, batería, pastillas de frenos. Esto genera un valor extra en la cuota (consultar previamente con el ejecutivo comercial)</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">8- ¿Qué incluye el seguro?</div><br>
					<p>El seguro en tu cuota de renting incluye cobertura de daños por accidente y robo parcial o total, daños a terceros en bienes y personas, gastos médicos para los ocupantes del vehículo asegurado.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">9- Cuando finalice el contrato, ¿el carro es mío?</div><br>
					<p>No, renting te permite el uso del vehículo, pero éste no pasa a tu propiedad. Al finalizar el contrato deberás entregar el vehículo y realizar la gestión para elegir tu carro nuevo.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">10- ¿Qué sucede si excedo los kilómetros contratados?</div><br>
					<p>Puedes controlar los kilómetros recorridos en la App de Renting CrediQ, y el ejecutivo de flota te recomendara, si es necesario, un ascenso.  La revisión de kilómetros contratados se hace anualmente, si te excedes en los kilómetros contratados, tendrás la opción de pagar la penalidad hasta la fecha y/o realizar un ascenso, y distribuir la penalidad en el resto del plazo.  Al finalizar el contrato se evalúan los kilómetros totales y, si hay exceso, se aplica una penalidad por cada kilómetro adicional.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">11- ¿Qué pasa si deseo entregar el carro antes de finalizar el contrato?</div><br>
					<p>El contrato de renting puede ser finalizado en cualquier momento con aviso de 30 días previo a la fecha deseada y se pagará una penalidad por entrega anticipada.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">12- ¿Qué tengo que hacer cuando finalice el contrato?</div><br>
					<p>Cuando tu contrato este por finalizar, el ejecutivo de flotas te contactará para coordinar la recepción en las oficinas de CrediQ. Se realizará inspección de las condiciones físicas y mecánicas del vehículo, los kilómetros recorridos, y se determinará si existen penalidades. Paga las penalidades en la caja de CrediQ, en caso de que existan, y posteriormente se te reembolsará el depósito inicial.</p>
					<br>
					<div class="hr-line"></div>
					<br>
					<div class="title-head-sm-left">13- ¿Qué hacer si deseo contratar más kilometraje?</div><br>
					<p>Acércate a las oficinas de CrediQ y te haremos un ascenso que se acomode mejor a tus necesidades ajustando la cuota de éste.</p>
